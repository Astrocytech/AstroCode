def n_queens(n):
    def is_valid(board, row, col):
        for c in range(col):
            if board[row][c] == 1: return False
        offset = [-1, 1]
        for i, j in zip(range(row), range(col, n)):
            for k in offset:
                if board[i][j+k] == 1: return False
        return True
    def solve_n_queens_util(board, col):
        global result
        if col == n:
            result.append([x.copy() for x in board])
            return
        for i in range(n):
            if is_valid(board, col, i):
                board[col][i] = 1
                solve_n_queens_util(board, col + 1)
                board[col][i] = 0
    result = []
    n = 4
    board = [[0]*n for _ in range(n)]
    solve_n_queens_util(board, 0)
    return result
